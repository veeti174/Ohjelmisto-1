huone = input("Mika on huoneesi? ")

if huone == "LUX"  :
    print("LUX on parvekkeellinen hytti ylakannella.")

elif (huone == "A" ):
    print ("A on ikkunallinen hytti autokannen ylapuolella.")

elif (huone == "B" ):
    print ("B on ikkunaton hytti autokannen ylapuolella.")

elif (huone == "C" ):
    print ("C on ikkunaton hytti autokannen alapuolella.")

else:
    print("Sulla ei ole huonetta taalla, joten mene pois.")